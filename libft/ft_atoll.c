#include "libft.h"

long long	ft_atoll(const char *str)
{
	long long	atoll;
	long long	sign;
	long long	i;

	if (!str)
		return (0);
	sign = 1;
	atoll = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		atoll *= 10;
		atoll += str[i] - '0';
		i++;
	}
	return (sign * atoll);
}
